char	*ft_strcpy(char *d, const char *s)
{
	int		i;

	i = 0;
	while (s[i] != '\0' || d[i] != '\0')
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
